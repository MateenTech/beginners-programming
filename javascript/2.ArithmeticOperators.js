//                  Compound Assignments
/*
    Compound Assignment Operators are:

    += , -=, *=, /=
*/

let a = 2;
a += 5; // Assign value of 5 to by adding to what ever the initial value of a is
console.log(a) // 7

a -= 2; // Assign by subtracting to whatever the value of a is
console.log(a) // 5

a *= 5; // Assing by multiplying to whatever the value of a is
console.log(a) // 25

a /= 2;
console.log(a) // 12.5



// Increment of a number by (++) operator
let myNum = 1;
myNum++; // is same as i = i + 1
console.log(myNum) // 2

// Decrement of a number by (--) operator
let myNumX = 11;
myNumX--; 
console.log(myNumX); // 10